package com.corejava.java8.stream.lamdaexpression;

public class Overview {

	/*
	 * Lambda expression facilitates functional programming, and simplifies the
	 * development a lot.
	 * 
	 * (argument-list) -> {body}
	 */

	/*
	 * Java lambda expression is treated as a function, so compiler does not create
	 * .class file.
	 */

	/*
	 * Functional Interface:
	 * 
	 * 
	 * An interface which has only one abstract method is called functional
	 * interface.
	 * 
	 * Java provides an annotation @FunctionalInterface, which is used to declare an
	 * interface as functional interface.
	 * 
	 * Since default methods have an implementation, they are not abstract.
	 * 
	 * If an interface declares an abstract method overriding one of the public
	 * methods of java.lang.Object, that also does not count toward the interface's
	 * abstract method count since any implementation of the interface will have an
	 * implementation from java.lang.Object or elsewhere.
	 * 
	 * e.g. Runnable, Comparator
	 * 
	 * Lambda expression provides implementation of functional interface.
	 * 
	 */
}
